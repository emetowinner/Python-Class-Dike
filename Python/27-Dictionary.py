# How to create a dictionary in python
mydict = {
    "brand":"Ford",
    "model":"Mustang",
    "year":2021,
    "color":["red","white","blue"]
}
print(mydict)

# How to refer to a value uin a dictionary
print(mydict["brand"])

# How to change a value in a dictionary
mydict["brand"] = "Honda"
print(mydict)

