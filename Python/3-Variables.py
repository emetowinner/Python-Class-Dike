# Creating Variables
x = 5
y = "John"
print(x)
print(y)

x = 4 # x is of type INT
x = "Winner" # x is of type STR
print(x)

# Casting
x = str(3) # x will be "3"
y = int(3) # y will be 3
z = float(3) # z will be 3.0

print(x)
print(y)
print(z)

# Get The Type
x = 5
y = "John"
print(type(x))
print(type(y))

# Single or Double Quotes
x = "John"
# is the same as
x = 'John'

# Case-Sensitive
a = 4
A = "Winner"
